package com.example.micrometer;

import org.springframework.data.cassandra.core.mapping.PrimaryKey;
import org.springframework.data.cassandra.core.mapping.Table;

import java.util.UUID;

@Table
public class SampleEntity {

    @PrimaryKey
    private String id;

    private String description;

    public SampleEntity() {
    }

    public SampleEntity(String description) {
        this(UUID.randomUUID().toString(), description);
    }

    public SampleEntity(String id, String description) {
        this.id = id;
        this.description = description;
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescription() {
        return this.description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return "SampleEntity{id='%s', description='%s'}".formatted(this.id, this.description);
    }

}
